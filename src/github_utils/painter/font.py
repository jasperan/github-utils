GLYPH_WIDTH = 5
GLYPH_HEIGHT = 7
GAP = 1  # 1-column gap between characters


class PixelFont:
    """5x7 pixel font for rendering text on contribution graphs."""

    @property
    def glyphs(self) -> Dict[str, List[List[int]]]:
        return _GLYPHS

    def get_glyph(self, ch: str) -> List[List[int]] | None:
        """Return the 7x5 bitmap for *ch* (upper-cased), or ``None``."""
        return _GLYPHS.get(ch.upper())


def render_text(
    text: str,
    intensity: int = 4,
    anchor_date: date | None = None,
) -> ContributionGrid:
    """Render *text* onto a new :class:`ContributionGrid`.

    Each character occupies 5 columns with a 1-column gap between characters.
    Characters that would extend past column 51 are silently dropped.
    """
    grid = ContributionGrid.empty(anchor_date)
    font = PixelFont()
    col_cursor = 0

    for ch in text.upper():
        glyph = font.get_glyph(ch)
        if glyph is None:
            # Unknown character -- skip it entirely.
            continue

        # Would this glyph overflow past the last column?
        if col_cursor + GLYPH_WIDTH > ContributionGrid.COLS:
            break

        for row_idx in range(GLYPH_HEIGHT):
            for col_idx in range(GLYPH_WIDTH):
                if glyph[row_idx][col_idx]:
                    grid.set(row_idx, col_cursor + col_idx, intensity)

        col_cursor += GLYPH_WIDTH + GAP

    return grid
